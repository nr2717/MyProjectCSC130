package project;

public class Item
{
	private String name;
	private float cost;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public float getCost()
	{
		return cost;
	}
	
	public void setCost(float cost)
	{
		this.cost = cost;
	}
	
	
}
